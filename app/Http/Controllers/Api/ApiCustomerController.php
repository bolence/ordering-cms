    {
        $customer_orders = Customer::with(['orders' => function ($q) use ($id) {
            $q->with('order_items', 'status')->where('customer_id', $id);
        }])->findOrFail($id);

        return response()->json([
            'customer_orders' => $customer_orders
        ], 200);
    }

